use crate::{
    registry::Registry,
    system::schedule::{
        Scheduler,
        Stages,
    },
};

pub trait Sealed<
    'a,
    R,
    Resources,
    I,
    P,
    RI,
    MergeParametersList,
    ResourcesIndicesLists,
    ResourcesContainmentsLists,
    ResourcesInverseIndicesLists,
    QueryIndicesLists,
    ResourceViewsIndicesLists,
    EntryViewsContainmentsLists,
    EntryViewsIndicesLists,
    EntryViewsReshapeIndicesLists,
    EntryViewsInverseIndicesLists,
    EntryViewsOppositeContainmentsLists,
    EntryViewsOppositeIndicesLists,
    EntryViewsOppositeReshapeIndicesLists,
    EntryViewsOppositeInverseIndicesLists,
    EntryContainmentsLists,
    EntryIndicesLists,
    EntryReshapeIndicesLists,
> where
    R: Registry,
{
    type Stages: Stages<
        'a,
        R,
        Resources,
        QueryIndicesLists,
        ResourceViewsIndicesLists,
        EntryViewsContainmentsLists,
        EntryViewsIndicesLists,
        EntryViewsReshapeIndicesLists,
        EntryViewsInverseIndicesLists,
        EntryViewsOppositeContainmentsLists,
        EntryViewsOppositeIndicesLists,
        EntryViewsOppositeReshapeIndicesLists,
        EntryViewsOppositeInverseIndicesLists,
        EntryContainmentsLists,
        EntryIndicesLists,
        EntryReshapeIndicesLists,
    >;

    fn as_stages(&'a mut self) -> Self::Stages;
}

impl<
        'a,
        R,
        Resources,
        T,
        I,
        P,
        RI,
        MergeParametersList,
        ResourcesIndicesLists,
        ResourcesContainmentsLists,
        ResourcesInverseIndicesLists,
        QueryIndicesLists,
        ResourceViewsIndicesLists,
        EntryViewsContainmentsLists,
        EntryViewsIndicesLists,
        EntryViewsReshapeIndicesLists,
        EntryViewsInverseIndicesLists,
        EntryViewsOppositeContainmentsLists,
        EntryViewsOppositeIndicesLists,
        EntryViewsOppositeReshapeIndicesLists,
        EntryViewsOppositeInverseIndicesLists,
        EntryContainmentsLists,
        EntryIndicesLists,
        EntryReshapeIndicesLists,
    >
    Sealed<
        'a,
        R,
        Resources,
        I,
        P,
        RI,
        MergeParametersList,
        ResourcesIndicesLists,
        ResourcesContainmentsLists,
        ResourcesInverseIndicesLists,
        QueryIndicesLists,
        ResourceViewsIndicesLists,
        EntryViewsContainmentsLists,
        EntryViewsIndicesLists,
        EntryViewsReshapeIndicesLists,
        EntryViewsInverseIndicesLists,
        EntryViewsOppositeContainmentsLists,
        EntryViewsOppositeIndicesLists,
        EntryViewsOppositeReshapeIndicesLists,
        EntryViewsOppositeInverseIndicesLists,
        EntryContainmentsLists,
        EntryIndicesLists,
        EntryReshapeIndicesLists,
    > for T
where
    R: Registry,
    T: Scheduler<
        'a,
        R,
        Resources,
        I,
        P,
        RI,
        MergeParametersList,
        ResourcesIndicesLists,
        ResourcesContainmentsLists,
        ResourcesInverseIndicesLists,
        QueryIndicesLists,
        ResourceViewsIndicesLists,
        EntryViewsContainmentsLists,
        EntryViewsIndicesLists,
        EntryViewsReshapeIndicesLists,
        EntryViewsInverseIndicesLists,
        EntryViewsOppositeContainmentsLists,
        EntryViewsOppositeIndicesLists,
        EntryViewsOppositeReshapeIndicesLists,
        EntryViewsOppositeInverseIndicesLists,
        EntryContainmentsLists,
        EntryIndicesLists,
        EntryReshapeIndicesLists,
    >,
{
    type Stages = <T as Scheduler<
        'a,
        R,
        Resources,
        I,
        P,
        RI,
        MergeParametersList,
        ResourcesIndicesLists,
        ResourcesContainmentsLists,
        ResourcesInverseIndicesLists,
        QueryIndicesLists,
        ResourceViewsIndicesLists,
        EntryViewsContainmentsLists,
        EntryViewsIndicesLists,
        EntryViewsReshapeIndicesLists,
        EntryViewsInverseIndicesLists,
        EntryViewsOppositeContainmentsLists,
        EntryViewsOppositeIndicesLists,
        EntryViewsOppositeReshapeIndicesLists,
        EntryViewsOppositeInverseIndicesLists,
        EntryContainmentsLists,
        EntryIndicesLists,
        EntryReshapeIndicesLists,
    >>::Stages;

    #[inline]
    fn as_stages(&'a mut self) -> Self::Stages {
        self.as_stages()
    }
}
